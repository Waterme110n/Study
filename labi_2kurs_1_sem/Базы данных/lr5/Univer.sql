Select AUDITORIUM.AUDITORIUM, AUDITORIUM.AUDITORIUM_CAPACITY, AUDITORIUM.AUDITORIUM_TYPE
From AUDITORIUM 
where AUDITORIUM.AUDITORIUM_CAPACITY in (
Select top(1) a.AUDITORIUM_CAPACITY 
From AUDITORIUM a
where a.AUDITORIUM_TYPE = AUDITORIUM.AUDITORIUM_TYPE
order by a.AUDITORIUM_CAPACITY desc)
order by AUDITORIUM.AUDITORIUM_CAPACITY desc
