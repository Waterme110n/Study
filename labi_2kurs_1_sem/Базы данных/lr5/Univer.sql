Select PULPIT.PULPIT_NAME
From PULPIT,FACULTY
Where FACULTY.FACULTY = PULPIT.FACULTY 
and PULPIT.FACULTY in (Select FACULTY From PROFESSION
Where (PROFESSION_NAME LIKE '%технологии%' or PROFESSION_NAME LIKE '%технология%'))
/*2*/
Select PULPIT.PULPIT_NAME
From PULPIT inner join Faculty 
ON FACULTY.FACULTY = PULPIT.FACULTY
Where PULPIT.FACULTY in (Select FACULTY From PROFESSION
Where (PROFESSION_NAME LIKE '%технологии%' or PROFESSION_NAME LIKE '%технология%'))
/*3*/
Select DISTINCT PULPIT.PULPIT_NAME
From PULPIT 
inner join FACULTY ON FACULTY.FACULTY = PULPIT.FACULTY 
inner join PROFESSION ON PROFESSION.FACULTY = PULPIT.FACULTY 
Where (PROFESSION_NAME LIKE '%технологии%' or PROFESSION_NAME LIKE '%технология%')
