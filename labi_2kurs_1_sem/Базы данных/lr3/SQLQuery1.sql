CREATE table Маршрут
(	Номер_маршрута int primary key,
	Название_маршрута nvarchar(50) not null,
	Дальность int not null,
	Количество_дней_в_пути int,
	Оплата money not null) on FG2;

CREATE table Перевозки
(	Номер_перевозки int primary key,
	Номер_маршрута int foreign key references Маршрут(Номер_маршрута),
	Индекс_водителя int foreign key references Водитель(Индекс_водителя),
	Дата_отправки date,
	Дата_прибытия date,) on FG3;

