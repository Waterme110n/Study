SELECT Водитель.Индекс_Водителя AS Индекс_Водителя, Маршрут.Номер_маршрута AS Номер_маршрута, Перевозки.Номер_перевозки AS Номер_перевозки,
      CASE 
	  WHEN Перевозки.Номер_перевозки = 123 THEN 'сто двадцать три'
	  WHEN Перевозки.Номер_перевозки = 003 THEN 'три'
	  END AS Номер_перевозки 
	  FROM Перевозки INNER JOIN Водитель ON Перевозки.Индекс_Водителя = Водитель.Индекс_Водителя
	                INNER JOIN Маршрут ON Перевозки.Номер_маршрута = Маршрут.Номер_маршрута
      WHERE Перевозки.Номер_перевозки BETWEEN 2 AND 124
	  ORDER BY Перевозки.Номер_перевозки DESC

