DECLARE
    -- Объявляем тип записи для сообщения
    TYPE message_rec IS RECORD (
        message_id NUMBER,
        user_id NUMBER,
        content VARCHAR2(1000)
    );

    -- Объявляем коллекцию для сообщений
    TYPE message_tab IS TABLE OF message_rec;
    messages_to_update message_tab;

BEGIN
    -- Извлекаем сообщения из таблицы messages
    SELECT message_id, user_id, content 
    BULK COLLECT INTO messages_to_update
    FROM messages;

    -- Обновляем содержимое каждого сообщения
    FORALL i IN 1 .. messages_to_update.COUNT
        UPDATE messages
        SET content = messages_to_update(i).content || ' - update'
        WHERE message_id = messages_to_update(i).message_id;

    COMMIT;
END;

