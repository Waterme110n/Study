SELECT 
    u.user_id,
    u.username,
    u.email,
    m.message_id,
    m.content
FROM users u
LEFT JOIN (
    SELECT 
        m.message_id,
        m.user_id,
        m.content
    FROM messages m 
) m ON m.user_id = u.user_id;

