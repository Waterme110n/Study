DECLARE
    k1 user_table;
    v_found BOOLEAN;
BEGIN
    -- Выборка данных 
    SELECT user_obj(user_id, username, email, 
                    CAST(MULTISET(
                        SELECT message_obj(message_id, user_id, content) 
                        FROM messages WHERE messages.user_id = users.user_id) AS message_table))
    BULK COLLECT INTO k1 FROM users;
    
    -- Выводим всех пользователей и их сообщения
    FOR i IN 1..k1.COUNT LOOP
        DBMS_OUTPUT.PUT_LINE('Пользователь: ' || k1(i).username);
        FOR j IN 1..k1(i).messages.COUNT LOOP
            DBMS_OUTPUT.PUT_LINE('Сообщение: ' || k1(i).messages(j).content);
        END LOOP;
    END LOOP;
    
    -- Ищем пересечения сообщений между пользователями
    FOR i IN 1..k1.COUNT LOOP
        FOR j IN 1..k1(i).messages.COUNT LOOP
            FOR k IN i+1..k1.COUNT LOOP
                FOR m IN 1..k1(k).messages.COUNT LOOP
                    IF k1(i).messages(j).content = k1(k).messages(m).content THEN
                        DBMS_OUTPUT.PUT_LINE('Пользователь ' || k1(i).username || 
                                             ' пересекается с пользователем ' || k1(k).username ||
                                             ' по сообщению: ' || k1(i).messages(j).content);
                    END IF;
                END LOOP;
            END LOOP;
        END LOOP;
    END LOOP;
    
    -- Ищем пользователя с user_id = 1 
    v_found := FALSE;
    FOR i IN 1..k1.COUNT LOOP
        IF k1(i).user_id = 1 THEN   
            v_found := TRUE;
            EXIT;
        END IF;
    END LOOP;
    IF v_found THEN
        DBMS_OUTPUT.PUT_LINE('Пользователь с ID=1 найден');
    ELSE
        DBMS_OUTPUT.PUT_LINE('Пользователь с ID=1 не найден');
    END IF;
    
    -- Находим пользователей без сообщений
    FOR i IN 1..k1.COUNT LOOP
        IF k1(i).messages IS EMPTY THEN
            DBMS_OUTPUT.PUT_LINE('Пользователь ' || k1(i).username || ' не имеет сообщений');
        END IF;
    END LOOP;
    
    -- Обмен сообщениями между двумя пользователями
    IF k1.COUNT > 1 THEN
        DECLARE
            temp_messages message_table;
        BEGIN
            temp_messages := k1(1).messages;
            k1(1).messages := k1(2).messages;
            k1(2).messages := temp_messages;
            DBMS_OUTPUT.PUT_LINE('Сообщения между пользователями 1 и 2 обменяны');
            
            DBMS_OUTPUT.PUT_LINE('Сообщения пользователя 1:');
            FOR j IN 1..k1(1).messages.COUNT LOOP
                DBMS_OUTPUT.PUT_LINE('Сообщение: ' || k1(1).messages(j).content);
            END LOOP;
            
            DBMS_OUTPUT.PUT_LINE('Сообщения пользователя 2:');
            FOR j IN 1..k1(2).messages.COUNT LOOP
                DBMS_OUTPUT.PUT_LINE('Сообщение: ' || k1(2).messages(j).content);
            END LOOP;
        END;
    END IF;
END;

