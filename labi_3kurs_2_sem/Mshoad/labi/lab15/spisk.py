# Работа со списками
fruits = ["яблоко", "банан", "вишня"]
print("Список фруктов:", fruits)

# Добавление элемента
fruits.append("апельсин")
print("После добавления апельсина:", fruits)

# Работа с кортежами
colors = ("красный", "зеленый", "синий")
print("Кортеж цветов:", colors)

# Доступ к элементам кортежа
print("Первый цвет:", colors[0])

# Работа со словарями
person = {
    "имя": "Алексей",
    "возраст": 25,
    "город": "Москва"
}
print("Словарь человека:", person)

# Доступ к значениям словаря
print("Имя:", person["имя"])
print("Возраст:", person["возраст"])

# Добавление нового ключа
person["профессия"] = "инженер"
print("После добавления профессии:", person)
